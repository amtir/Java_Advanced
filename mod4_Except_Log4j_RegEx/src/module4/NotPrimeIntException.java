package module4;

public class NotPrimeIntException extends Exception {

	private static final long serialVersionUID = -5807915051996596234L;
	
	public NotPrimeIntException(String message) {
		super(message);
	}
	
	@Override
	public String getMessage() {
		return super.getMessage();
	}

	@Override
	public String toString() {
		return super.toString();
	}

}
